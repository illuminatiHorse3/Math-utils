package org.javatime.mathutils.shape;

public class Rhombus{

    public static Integer area(Integer a, Integer b){
        return (a * b)/2;
    }
    public static Long area(Long a, Long b){
        return (a*b)/2;
    }
    public static Double area(Double a, Double b){
        return (a*b)/2;
    }


    public static Integer perimeter(Integer a){
        return 4*a;
    }
    public static Long perimeter(Long a){
        return 4*a;
    }
    public static Double perimeter(Double a){
        return 4*a;
    }
}
